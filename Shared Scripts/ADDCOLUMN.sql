CREATE OR REPLACE PROCEDURE "ADDCOLUMN"("CLIENTNAME" VARCHAR(16777216), "TABLENAME" VARCHAR(16777216), "COLUMNNAME" VARCHAR(16777216), "COLUMNTYPE" VARCHAR(16777216))
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
BEGIN
    EXECUTE IMMEDIATE ''ALTER TABLE '' || ClientName || ''.'' || TableName || '' ADD COLUMN IF NOT EXISTS '' || ColumnName || '' '' || ColumnType;
END;
';